#include <unistd.h>

void	ft_ptc(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		nb = 147483648;
		ft_ptc('-');
		ft_ptc('2');
	}
	if (nb < 0)
	{
		ft_ptc('-');
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	ft_ptc((nb % 10) + '0');
}

